DROP TABLE IF EXISTS ports;
-- Table for US Soil CBP Ports
CREATE TABLE ports(
	port_code INTEGER NOT NULL,
	street_address VARCHAR NOT NULL,
	city VARCHAR NOT NULL,
	state CHAR(2) NOT NULL,
	postal_code VARCHAR NOT NULL,
	field VARCHAR,
	PRIMARY KEY (port_code)
);

DROP TABLE IF EXISTS border_crossing;
-- Table for Border Crossing
CREATE TABLE border_crossing(
	id INTEGER NOT NULL,
	port_name VARCHAR NOT NULL,
	state VARCHAR NOT NULL,
	port_code INTEGER NOT NULL,
	border VARCHAR NOT NULL,
	date DATE NOT NULL,
	measure VARCHAR NOT NULL,
	value INTEGER NOT NULL,
	latitude FLOAT NOT NULL,
	longitude FLOAT NOT NULL,
	PRIMARY KEY (id)
);

